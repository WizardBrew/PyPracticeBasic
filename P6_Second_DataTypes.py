# These are the Types of data (Tuple, List, Set, Dict)

A = ("Apple", "Orange", "Mango", "Graph")       # Tuple
B = ('Apple', 'Orange', 'Mango', 'Graph')       # Tuple
C = ["Apple", "Orange", "Mango", "Graph"]       # List
D = ['Apple', 'Orange', 'Mango', 'Graph']       # List
E = {'Apple', 'Orange', 'Mango', 'Graph'}       # Set
F = {"Apple", "Orange", "Mango", "Graph"}       # Set
G = {'Apple':"Iphone", 'Orange': 'Verizone'}    #Dict
H = {"Apple":"Iphone", "Orange": "Verizone"}    #Dict

print(type(A))
print(type(B))
print(type(C))
print(type(D))
print(type(E))
print(type(F))
print(type(G))
print(type(H))
